public class UnknownViticultureDataParserException extends Exception {

    public UnknownViticultureDataParserException() {
    }

    public UnknownViticultureDataParserException(String message) {
        super(message);
    }

    public UnknownViticultureDataParserException(String message, Throwable cause) {
        super(message, cause);
    }

    public UnknownViticultureDataParserException(Throwable cause) {
        super(cause);
    }

    public UnknownViticultureDataParserException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
        super(message, cause, enableSuppression, writableStackTrace);
    }

}
